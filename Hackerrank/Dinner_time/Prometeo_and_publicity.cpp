#include <cmath>
#include <cstdio>
#include <vector>
#include <iostream>
#include <algorithm>
using namespace std;

bool isPerfectSquare(long long int x) 
{   
  // Find floating point value of  
  // square root of x. 
  long double sr = sqrt(x); 
  
  // If square root is an integer 
  return ((sr - floor(sr)) == 0); 
}

int main() {
    /* Enter your code here. Read input from STDIN. Print output to STDOUT */  
    int t;
    cin>>t;
    while(t--)
    {
        long long int n,m,x,p;
        cin>>n>>m>>x;
        if(((n*m)%x)==0)
        {
           p= n*m/x;
            if(isPerfectSquare(p))
               cout<<"YES"<<endl;
             else
               cout<<"NO"<<endl;
        }
        else
       cout<<"NO"<<endl;
    }
    return 0;
}
