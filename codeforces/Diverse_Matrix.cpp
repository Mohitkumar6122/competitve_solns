#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    int n,m;
    int k=0;
	cin>>n>>m;
    int a[n*m];
	if(n==1 && m==1)
    {
		cout<<"0";
		return 0;
	}
	if(m==1)
    {
		for(int i=0;i<n;i++)
        {
			cout<<i+2<<endl;
		}
		return 0;
	}
	k=n+1;
	for(int i=0;i<m;i++)
    {
		a[i]=k+i;
	}
	for(int i=1;i<=n;i++)
    {
		for(int j=0;j<m;j++)
        {
			cout<<a[j]*i<<" ";
		}
		cout<<endl;
	}
}
