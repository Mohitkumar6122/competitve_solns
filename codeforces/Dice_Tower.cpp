int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        long long int d,num,top;
        cin>>num;
        d=num/14;
        top = num - (14*d);
        if(d>=1 && top<=6 && top>=1)
        cout<<"YES"<<endl;
        else
        cout<<"NO"<<endl;
    }
}
