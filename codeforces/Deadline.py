for i in range(int(input())):
    n,d=map(int,input().split())
    cnt = d-1
    ans=0
    while cnt!=(-1):
        p = math.ceil(d/(cnt+1))
        #print("p = ",p," cnt = ",cnt)
        if (p+cnt)<=n:
            ans=1
            break
        cnt = cnt-1
    if ans==1:
        print("YES")
    else:
        print("NO")
