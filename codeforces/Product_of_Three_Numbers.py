import math
 
for i in range(int(input())):
    n = int(input())
    l = []
    p =math.sqrt(n)
    p = int(p)
    i=2
    while len(l)<2 and i<p:
        if n%i==0:
            l.append(i)
            n=n//i
        i+=1
    if len(l)==2 and n not in l:
        print("YES")
        print(*l,n)
    else:
        print("NO")
