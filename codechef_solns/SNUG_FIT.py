import math

for i in range(int(input())):
    n= int(input())
    a = list(map(int,input().split()))
    b = list(map(int,input().split()))
    a.sort()
    b.sort()
    s = 0
    for j in range(0,n):
        s = s + min(a[j],b[j])
    print(s)
