#include<bits/stdc++.h>
using namespace std;

int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        string s;
        cin>>s;
        int sa,sb,aindex,bindex;
        cin>>sa>>sb;
        for(int i=0;i<s.length();i++)
        {
            if(s[i]=='A')
            aindex = i;
            else if(s[i]=='B')
            bindex = i;
        }
        bool flag =false;
        for(int i=0;i<=s.length()/2;i++)
        {
            aindex = sa + aindex;
            bindex = bindex -sb;
            if(aindex == bindex)
            {
                flag = true;
                break;
            }
            flag =false;
        }
        if(flag)
        cout<<"unsafe"<<endl;
        else
        cout<<"safe"<<endl;
    }
    return 0;
}
